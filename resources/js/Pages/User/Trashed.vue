<script setup>
import {Head, Link, useForm, usePage } from '@inertiajs/vue3';
import { Inertia } from '@inertiajs/inertia';
import AuthenticatedLayout from "@/Layouts/AuthenticatedLayout.vue";
const form = useForm({});
const users = usePage().props.users;
const deleteUser = async (id) => {
    if (confirm('Are you sure you want to delete ?')) {
        try {
            Inertia.delete(route('users.delete', id));
        } catch (error) {
            console.error('Error deleting item:', error);
            // Handle error as needed
        }
    }
};
</script>
<template>
    <Head title="Dashboard" />

    <AuthenticatedLayout>
        <template #header>
            <h2 class="font-semibold text-xl text-gray-800 dark:text-gray-200 leading-tight">Dashboard</h2>
        </template>

        <div class="py-12">
            <div class="max-w-7xl mx-auto sm:px-6 lg:px-8">
                <div class="bg-white dark:bg-gray-800 overflow-hidden shadow-sm sm:rounded-lg">
                    <div class="p-6 text-gray-900 dark:text-gray-100">
                        <div class="mb-4 flex justify-between">
                            <div class="flex gap-4">
                                <Link
                                    class="bg-green-500 text-white p-3 inline-block"
                                    :href="route('users.index')"
                                >
                                    Active Users
                                </Link>
                                <Link
                                    class="bg-red-400 text-white p-3 inline-block"
                                    :href="route('users.trashed')"
                                >
                                    Trash Users
                                </Link>
                            </div>
                            <div>
                                <Link
                                    class="bg-blue-500 text-white p-3 inline-block"
                                    :href="route('users.create')"
                                >
                                    Create User
                                </Link>
                            </div>
                        </div>
                        <div>
                            <h3>Trash Users</h3>
                            <table class="user-table text-center w-full">
                                <thead>
                                <tr>
                                    <th>Name</th>
                                    <th>Action</th>
                                </tr>
                                </thead>
                                <tbody>
                                <tr v-for="user in users">
                                    <td>{{user.firstname}}</td>
                                    <td class="w-[30%]">
                                        <div class="flex gap-1 justify-center">
                                            <form @submit.prevent="form.patch(route('users.restore', user.id))" >
                                                <button
                                                    class="p-2 bg-amber-500 text-white"
                                                >
                                                    Restore
                                                </button>
                                            </form>
                                            <button
                                                class="p-2 bg-red-500 text-white"
                                                @click=deleteUser(user.id)
                                            >
                                                Delete
                                            </button>
                                        </div>
                                    </td>
                                </tr>
                                </tbody>
                            </table>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </AuthenticatedLayout>
</template>
